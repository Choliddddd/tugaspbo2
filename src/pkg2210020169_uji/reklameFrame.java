    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // Ambil ID reklame dari field teks
            String kd_reklame = txtKrek.getText();

            // Pastikan ID reklame tidak kosong
            if (kd_reklame.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID reklame tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ambil data baru dari field teks
            String nama_reklame = txtNrek.getText();
            String satuan = txtSatuan.getText();
            String harga = txtHarga.getText();

            // Query untuk mengupdate data reklame berdasarkan ID
            String sqlUpdate = "UPDATE reklame SET nama_reklame = ?, satuan = ?, harga = ? WHERE kd_reklame = ?";
            PreparedStatement perintahUpdate = KoneksiDatabase.prepareStatement(sqlUpdate);
            perintahUpdate.setString(1, nama_reklame);
            perintahUpdate.setString(2, satuan);
            perintahUpdate.setString(3, harga);
            perintahUpdate.setString(4, kd_reklame);

            int affectedRows = perintahUpdate.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID reklame tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahUpdate.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

