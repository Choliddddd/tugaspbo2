    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try {
            // Ambil ID karyawan dari field teks
            String id_karyawan = txtIK.getText();

            // Pastikan ID karyawan tidak kosong
            if (id_karyawan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Karyawan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Query untuk menghapus data karyawan berdasarkan ID
            String sqlHapus = "DELETE FROM karyawan WHERE id_karyawan = ?";
            PreparedStatement perintahHapus = KoneksiDatabase.prepareStatement(sqlHapus);
            perintahHapus.setString(1, id_karyawan);

            int affectedRows = perintahHapus.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID KARYAWAN tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahHapus.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // Ambil ID karyawan dari field teks
            String id_karyawan = txtIK.getText();

            // Pastikan ID karyawan tidak kosong
            if (id_karyawan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID KARYAWAN tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ambil data baru dari field teks
            String nama_karyawan = txtNK.getText();
            String alamat = txtAlamat.getText();
            String no_telp = txtNotelp.getText();
            String posisi = txtPosisi.getText();

            // Query untuk mengupdate data karyawan berdasarkan ID
            String sqlUpdate = "UPDATE karyawan SET nama_karyawan = ?, alamat = ?, no_telp = ?, posisi = ? WHERE id_karyawan = ?";
            PreparedStatement perintahUpdate = KoneksiDatabase.prepareStatement(sqlUpdate);
            perintahUpdate.setString(1, nama_karyawan);
            perintahUpdate.setString(2, alamat);
            perintahUpdate.setString(3, no_telp);
            perintahUpdate.setString(4, posisi);
            perintahUpdate.setString(5, id_karyawan);

            int affectedRows = perintahUpdate.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID Karyawan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahUpdate.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

