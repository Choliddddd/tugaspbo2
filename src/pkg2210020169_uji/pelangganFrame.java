    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
            // Ambil ID pelanggan dari field teks
            String id_pelanggan = txtIDP.getText();

            // Pastikan ID pelanggan tidak kosong
            if (id_pelanggan.isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID Pelanggan tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Ambil data baru dari field teks
            String nama = txtNP.getText();
            String no_telp = txtNotelp.getText();
            String nama_perusahaan = txtNperusahaan.getText();
            String alamat = txtAlamat.getText();

            // Query untuk mengupdate data pelanggan berdasarkan ID
            String sqlUpdate = "UPDATE pelanggan SET nama = ?, no_telp = ?, nama_perusahaan = ?, alamat = ? WHERE id_pelanggan = ?";
            PreparedStatement perintahUpdate = KoneksiDatabase.prepareStatement(sqlUpdate);
            perintahUpdate.setString(1, nama);
            perintahUpdate.setString(2, no_telp);
            perintahUpdate.setString(3, nama_perusahaan);
            perintahUpdate.setString(4, alamat);
            perintahUpdate.setString(5, id_pelanggan);

            int affectedRows = perintahUpdate.executeUpdate(); // Eksekusi query

            // Cek apakah ada baris yang terpengaruh
            if (affectedRows > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "ID PELANGGAN tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }

            // Tutup PreparedStatement
            perintahUpdate.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error saat mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditActionPerformed

